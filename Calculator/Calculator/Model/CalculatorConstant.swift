import Foundation

extension String {
    static let zero = "0"
    static let one = "1"
    static let blank = ""
    static let dot = "."
}

extension Int {
    static let zero = 0
    static let one = 1
    static let binary = 2
}

extension Double {
    static let zero = 0.0
}

extension UInt8 {
    static let uInt8Zero: UInt8 = 0
}
